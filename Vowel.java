class Vowel
{
    public static void main(String[] args)
    {
        char choice='A';
        switch(choice)
        {
        case 'A': System.out.println(choice +"is vowel");
        break;
        case 'E': System.out.println(choice +"is vowel");
        break;
        case 'I': System.out.println(choice +"is vowel");
        break;
        case 'O': System.out.println(choice +"is vowel");
        break;
        case 'U': System.out.println(choice +"is vowel");
        break;
        default: System.out.println(choice+"is not vowel");
        }
    }
}